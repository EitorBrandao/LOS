package entity;

public class Idolo {
	//codigo do idolo tem o TIPO, RARIDADE, SLOT e NIVEL MINIMO
	private String cod;
	
	//status do idolo
	private int poder;
	private int forca;
	private int crescimento;
	private int fusao;
	private int sincronia;
	private int tamanho;
	private int formato;
	private int critico;
	private int bloqueio;
	
	public String getCod() {
		return cod;
	}
	public void setCod(String cod) {
		this.cod = cod;
	}
	public int getPoder() {
		return poder;
	}
	public void setPoder(int poder) {
		this.poder = poder;
	}
	public int getForca() {
		return forca;
	}
	public void setForca(int forca) {
		this.forca = forca;
	}
	public int getCrescimento() {
		return crescimento;
	}
	public void setCrescimento(int crescimento) {
		this.crescimento = crescimento;
	}
	public int getFusao() {
		return fusao;
	}
	public void setFusao(int fusao) {
		this.fusao = fusao;
	}
	public int getSincronia() {
		return sincronia;
	}
	public void setSincronia(int sincronia) {
		this.sincronia = sincronia;
	}
	public int getTamanho() {
		return tamanho;
	}
	public void setTamanho(int tamanho) {
		this.tamanho = tamanho;
	}
	public int getFormato() {
		return formato;
	}
	public void setFormato(int formato) {
		this.formato = formato;
	}
	public int getCritico() {
		return critico;
	}
	public void setCritico(int critico) {
		this.critico = critico;
	}
	public int getBloqueio() {
		return bloqueio;
	}
	public void setBloqueio(int bloqueio) {
		this.bloqueio = bloqueio;
	}
	
	
}
